package chapters.chapter2;

public class IntExample {
    public static void main(String[] args) {
        int num = 100;
        System.out.println("Սա փոփոխական թիվ է։ " + num);
        num = num * 2;
        System.out.println("Փոփոխականի անունը num * 2 հավասար է ");
        System.out.println(num);
    }
}
