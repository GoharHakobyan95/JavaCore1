package chapters.chapter2;

public class BlockExample {
    public static void main(String[] args) {
        int x, y;
        y = 20;
        for (x = 0; x < 10; x++) {
            System.out.println("Նշանակությունը x: " + x);
            System.out.println("Նշանակությունը y: " + y);
            y = y - 2;
        }

    }
}
